public class House {
    String type;
    int price;
    int squareMeters;
    int numberOfRooms;
    int numberOfHalls;

    public House(String type, int price, int squareMeters, int numberOfRooms, int numberOfHalls) {
        this.type = type;
        this.price = price;
        this.squareMeters = squareMeters;
        this.numberOfRooms = numberOfRooms;
        this.numberOfHalls = numberOfHalls;
    }
}
